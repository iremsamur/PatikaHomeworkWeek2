public class House {
    private int price;
    private double squareMeter;

    public House(int price, double squareMeter) {
        this.price = price;
        this.squareMeter = squareMeter;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public double getSquareMeter() {
        return squareMeter;
    }

    public void setSquareMeter(double squareMeter) {
        this.squareMeter = squareMeter;
    }




}
